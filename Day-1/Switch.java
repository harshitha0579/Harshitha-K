public class Switch{
    public static void main(String[] args){
        int num=6;
        switch(num){
            case 1:
                   System.out.println("RED");
                   break;
            case 2:
                 System.out.println("ORANGE");
                 break;
             case 3:
                   System.out.println("GREEN");
                   break;
            case 4:
                 System.out.println("YELLOW");
                 break;
           default:
                 System.out.println("default case");
        }
    }
}